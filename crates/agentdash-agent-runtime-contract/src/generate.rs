use std::{collections::BTreeSet, env, fs, path::Path};

use agentdash_agent_runtime_contract::{
    ManagedRuntimeContractSchema, ManagedRuntimeProjectionSchema, RuntimeU64,
};
use schemars::schema_for;
use ts_rs::TS;

fn main() {
    let check = env::args().any(|argument| argument == "--check");
    ensure_no_implicit_bindings();
    let root = Path::new(env!("CARGO_MANIFEST_DIR")).join("../..");
    let ts_path = root.join("packages/app-web/src/generated/agent-runtime-contracts.ts");
    let protocol_ts_path = root.join("packages/app-web/src/generated/backbone-protocol.ts");
    let validators_path = root.join("packages/app-web/src/generated/agent-runtime-validators.ts");
    let schema_path = root.join("schemas/agent-runtime-contract.schema.json");
    let temp = tempfile::tempdir().expect("create generation directory");
    ManagedRuntimeContractSchema::export_all_to(temp.path())
        .expect("export Managed Runtime contract types");
    ManagedRuntimeProjectionSchema::export_all_to(temp.path())
        .expect("export Managed Runtime projection types");
    RuntimeU64::export_all_to(temp.path()).expect("export canonical Runtime u64 wire scalar");
    let mut declarations = Vec::new();
    collect_typescript(temp.path(), &mut declarations);
    let protocol_types = exported_type_names(&protocol_ts_path);
    declarations.retain(|declaration| {
        declaration_name(declaration).is_none_or(|name| !protocol_types.contains(name))
    });
    declarations.sort();
    ensure_required_declarations(&declarations, &required_runtime_type_names());
    let typescript = format!(
        "// Generated by `cargo run -p agentdash-agent-runtime-contract --bin generate_agent_runtime_contracts`; do not edit.\n\nimport type {{ CanonicalConversationRecord }} from \"./backbone-protocol\";\n\n{}\n",
        declarations.join("\n\n")
    );
    ensure_no_bigint(&typescript);
    let json_schema = format!(
        "{}\n",
        serde_json::to_string_pretty(&schema_for!(ManagedRuntimeContractSchema))
            .expect("serialize Managed Runtime JSON Schema")
    );
    check_or_write(&ts_path, &typescript, check);
    check_or_write(&validators_path, MANAGED_RUNTIME_VALIDATORS, check);
    check_or_write(&schema_path, &json_schema, check);
}

fn ensure_no_bigint(source: &str) {
    if source
        .split(|character: char| !character.is_ascii_alphanumeric() && character != '_')
        .any(|token| token == "bigint")
    {
        panic!("Managed Runtime TypeScript wire contract must not contain bigint");
    }
}

const MANAGED_RUNTIME_VALIDATORS: &str = include_str!("agent_runtime_validators.ts");

fn required_runtime_type_names() -> BTreeSet<String> {
    [
        "ManagedRuntimeContractSchema",
        "ManagedRuntimeProjectionSchema",
        "ManagedRuntimeSnapshot",
        "ManagedRuntimePresentationContentBlock",
        "ManagedRuntimeItemBody",
        "ManagedRuntimeItemPresentation",
        "ManagedRuntimeItemTerminalEvidence",
        "ManagedRuntimeInteractionRequest",
        "ManagedRuntimeInteractionStatus",
        "ManagedRuntimeInteractionResolution",
        "RuntimeU64",
    ]
    .into_iter()
    .map(str::to_owned)
    .collect()
}

fn ensure_required_declarations(declarations: &[String], required: &BTreeSet<String>) {
    let emitted = declarations
        .iter()
        .filter_map(|declaration| declaration_name(declaration))
        .collect::<BTreeSet<_>>();
    let missing = required
        .iter()
        .filter(|name| !emitted.contains(name.as_str()))
        .cloned()
        .collect::<Vec<_>>();
    if !missing.is_empty() {
        panic!(
            "Managed Runtime export closure omitted declarations: {}",
            missing.join(", ")
        );
    }
}

fn declaration_name(declaration: &str) -> Option<&str> {
    declaration.lines().find_map(|line| {
        let line = line.trim_start();
        let remainder = line
            .strip_prefix("export type ")
            .or_else(|| line.strip_prefix("export interface "))?;
        remainder
            .split(|character: char| !character.is_ascii_alphanumeric() && character != '_')
            .next()
            .filter(|name| !name.is_empty())
    })
}

fn exported_type_names(path: &Path) -> BTreeSet<String> {
    fs::read_to_string(path)
        .expect("read canonical backbone protocol TypeScript")
        .lines()
        .filter_map(|line| declaration_name(line).map(str::to_owned))
        .collect()
}

fn ensure_no_implicit_bindings() {
    let crates = Path::new(env!("CARGO_MANIFEST_DIR")).join("..");
    if let Some(path) = find_implicit_bindings(&crates) {
        panic!(
            "{} must not exist; TypeScript contracts are generated only through explicit workspace generators",
            path.display()
        );
    }
}

fn find_implicit_bindings(directory: &Path) -> Option<std::path::PathBuf> {
    for entry in fs::read_dir(directory).ok()? {
        let path = entry.ok()?.path();
        if path.is_dir() {
            if path.file_name().is_some_and(|name| name == "bindings") {
                return Some(path);
            }
            if let Some(found) = find_implicit_bindings(&path) {
                return Some(found);
            }
        }
    }
    None
}

fn collect_typescript(directory: &Path, declarations: &mut Vec<String>) {
    for entry in fs::read_dir(directory).expect("read generated TypeScript directory") {
        let path = entry.expect("read generated entry").path();
        if path.is_dir() {
            collect_typescript(&path, declarations);
        } else if path.extension().is_some_and(|extension| extension == "ts") {
            let declaration = fs::read_to_string(path)
                .expect("read generated TypeScript")
                .lines()
                .filter(|line| !line.starts_with("// ") && !line.starts_with("import "))
                .collect::<Vec<_>>()
                .join("\n")
                .trim()
                .to_owned();
            if !declaration.is_empty() {
                declarations.push(declaration);
            }
        }
    }
}

fn check_or_write(path: &Path, expected: &str, check: bool) {
    if check {
        match fs::read_to_string(path) {
            Ok(actual) if actual == expected => {
                eprintln!("{} is up to date", path.display());
                return;
            }
            Ok(_) | Err(_) => {
                eprintln!(
                    "{} is out of date; regenerate during the S5 canonical artifact cut",
                    path.display()
                );
                std::process::exit(1);
            }
        }
    }
    fs::create_dir_all(path.parent().expect("generated parent")).expect("create generated parent");
    fs::write(path, expected).expect("write generated contract");
    eprintln!("wrote {}", path.display());
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    #[should_panic(expected = "must not contain bigint")]
    fn bigint_is_rejected_from_runtime_wire_contract() {
        ensure_no_bigint("export type Revision = bigint;");
    }

    #[test]
    fn required_closure_covers_complete_presentation_vocabulary() {
        let names = required_runtime_type_names();
        for required in [
            "ManagedRuntimeItemBody",
            "ManagedRuntimeItemPresentation",
            "ManagedRuntimeInteractionRequest",
        ] {
            assert!(names.contains(required), "missing {required}");
        }
    }

    #[test]
    #[should_panic(expected = "omitted declarations")]
    fn missing_presentation_declaration_is_generation_failure() {
        let declarations =
            vec!["export type ManagedRuntimeSnapshot = { revision: string };".to_owned()];
        let required = ["ManagedRuntimeSnapshot", "ManagedRuntimeItemBody"]
            .into_iter()
            .map(str::to_owned)
            .collect();
        ensure_required_declarations(&declarations, &required);
    }
}
