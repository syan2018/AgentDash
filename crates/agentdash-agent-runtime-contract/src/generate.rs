use std::{collections::BTreeMap, collections::BTreeSet, env, fs, path::Path};

use agentdash_agent_runtime_contract::{
    RuntimeContractSchema, ToolPresentationEmitter, ToolProtocolProjection,
};
use schemars::schema_for;
use ts_rs::TS;

fn main() {
    let check = env::args().any(|argument| argument == "--check");
    ensure_no_implicit_bindings();
    let root = Path::new(env!("CARGO_MANIFEST_DIR")).join("../..");
    let ts_path = root.join("packages/app-web/src/generated/agent-runtime-contracts.ts");
    let backbone_ts_path = root.join("packages/app-web/src/generated/backbone-protocol.ts");
    let validator_path = root.join("packages/app-web/src/generated/agent-runtime-validators.ts");
    let schema_path = root.join("schemas/agent-runtime-contract.schema.json");

    let temp = tempfile::tempdir().expect("create generation directory");
    RuntimeContractSchema::export_all_to(temp.path()).expect("export runtime contract types");
    ToolProtocolProjection::export_all_to(temp.path())
        .expect("export tool protocol projection descriptor");
    ToolPresentationEmitter::export_all_to(temp.path())
        .expect("export tool presentation emitter descriptor");
    let mut declarations = Vec::new();
    collect_typescript(temp.path(), &mut declarations);
    let backbone_source = fs::read_to_string(&backbone_ts_path).unwrap_or_else(|error| {
        panic!(
            "read Backbone bindings at {}: {error}; run the Backbone generator first",
            backbone_ts_path.display()
        )
    });
    let backbone_declarations = exported_type_declarations(&backbone_source);
    let backbone_names = backbone_declarations
        .keys()
        .cloned()
        .collect::<BTreeSet<_>>();
    declarations.retain(|declaration| {
        let Some(name) = declaration_name(declaration) else {
            return true;
        };
        let Some(backbone_declaration) = backbone_declarations.get(name) else {
            return true;
        };
        let runtime_declaration = exported_type_declaration(declaration, name)
            .unwrap_or_else(|| panic!("extract generated Runtime declaration for {name}"));
        assert_eq!(
            normalize_type_declaration(runtime_declaration),
            normalize_type_declaration(backbone_declaration),
            "generated Runtime type `{name}` collides with a different Backbone contract"
        );
        false
    });
    declarations.sort();
    let runtime_source = declarations.join("\n\n");
    let imports = referenced_type_names(&runtime_source, &backbone_names);
    assert!(
        imports.contains("BackboneEvent"),
        "Runtime presentation contract must import the exhaustive BackboneEvent union"
    );
    let import = if imports.is_empty() {
        String::new()
    } else {
        format!(
            "import type {{ {} }} from \"./backbone-protocol\";\nexport type {{ {} }} from \"./backbone-protocol\";\n\n",
            imports.iter().cloned().collect::<Vec<_>>().join(", "),
            imports.into_iter().collect::<Vec<_>>().join(", ")
        )
    };
    let typescript = format!(
        "// Generated by `cargo run -p agentdash-agent-runtime-contract --bin generate_agent_runtime_contracts`; do not edit.\n\n{import}{runtime_source}\n",
    );

    let schema = schema_for!(RuntimeContractSchema);
    let json_schema = format!(
        "{}\n",
        serde_json::to_string_pretty(&schema).expect("serialize runtime JSON Schema")
    );

    check_or_write(&ts_path, &typescript, check);
    check_or_write(&validator_path, RUNTIME_VALIDATORS, check);
    check_or_write(&schema_path, &json_schema, check);
}

const RUNTIME_VALIDATORS: &str = r#"// Generated by `cargo run -p agentdash-agent-runtime-contract --bin generate_agent_runtime_contracts`; do not edit.

import type { RuntimeEventEnvelope, RuntimeTransientCoordinate } from "./agent-runtime-contracts";

export type RuntimeContractValidationResult<T> =
  | { ok: true; value: T }
  | { ok: false; field_path: string; expected: string };

function isRecord(value: unknown): value is Record<string, unknown> {
  return value != null && typeof value === "object" && !Array.isArray(value);
}

function isWireInteger(value: unknown): value is number {
  return typeof value === "number" && Number.isSafeInteger(value) && value >= 0;
}

function isNonEmptyString(value: unknown): value is string {
  return typeof value === "string" && value.length > 0;
}

export function isGeneratedRuntimeTransientCoordinate(value: unknown): value is RuntimeTransientCoordinate {
  return isRecord(value)
    && isNonEmptyString(value.binding_id)
    && isWireInteger(value.stream_generation)
    && isWireInteger(value.sequence)
    && isNonEmptyString(value.event_id)
    && (value.turn_id === null || isNonEmptyString(value.turn_id));
}

export function isGeneratedRuntimeEventEnvelope(value: unknown): value is RuntimeEventEnvelope {
  if (!isRecord(value)) return false;
  return isNonEmptyString(value.thread_id)
    && isWireInteger(value.occurred_at_ms)
    && (value.sequence === null || isWireInteger(value.sequence))
    && (value.transient === null || isGeneratedRuntimeTransientCoordinate(value.transient))
    && ((value.sequence === null) !== (value.transient === null))
    && isWireInteger(value.revision)
    && isRecord(value.event)
    && isNonEmptyString(value.event.kind);
}

export function parseGeneratedRuntimeEventEnvelope(value: unknown): RuntimeContractValidationResult<RuntimeEventEnvelope> {
  if (!isRecord(value)) return { ok: false, field_path: "$", expected: "object" };
  if (!isNonEmptyString(value.thread_id)) return { ok: false, field_path: "thread_id", expected: "non-empty string" };
  if (!isWireInteger(value.occurred_at_ms)) return { ok: false, field_path: "occurred_at_ms", expected: "finite nonnegative safe integer" };
  if (!(value.sequence === null || isWireInteger(value.sequence))) return { ok: false, field_path: "sequence", expected: "finite nonnegative safe integer or null" };
  if (!(value.transient === null || isGeneratedRuntimeTransientCoordinate(value.transient))) return { ok: false, field_path: "transient", expected: "RuntimeTransientCoordinate or null" };
  if ((value.sequence === null) === (value.transient === null)) return { ok: false, field_path: "sequence/transient", expected: "exactly one durable sequence or transient coordinate" };
  if (!isWireInteger(value.revision)) return { ok: false, field_path: "revision", expected: "finite nonnegative safe integer" };
  if (!isRecord(value.event) || !isNonEmptyString(value.event.kind)) return { ok: false, field_path: "event.kind", expected: "non-empty string" };
  if (!isGeneratedRuntimeEventEnvelope(value)) return { ok: false, field_path: "$", expected: "RuntimeEventEnvelope" };
  return { ok: true, value };
}
"#;

fn ensure_no_implicit_bindings() {
    let crates = Path::new(env!("CARGO_MANIFEST_DIR")).join("..");
    if let Some(path) = find_implicit_bindings(&crates) {
        eprintln!(
            "{} must not exist; TypeScript contracts are generated only through explicit workspace generators",
            path.display()
        );
        std::process::exit(1);
    }
}

fn find_implicit_bindings(directory: &Path) -> Option<std::path::PathBuf> {
    for entry in fs::read_dir(directory).ok()? {
        let path = entry.ok()?.path();
        if path.is_dir() {
            if path.file_name().is_some_and(|name| name == "bindings") {
                return Some(path);
            }
            if let Some(found) = find_implicit_bindings(&path) {
                return Some(found);
            }
        }
    }
    None
}

fn collect_typescript(directory: &Path, declarations: &mut Vec<String>) {
    for entry in fs::read_dir(directory).expect("read generated TypeScript directory") {
        let path = entry.expect("read generated entry").path();
        if path.is_dir() {
            collect_typescript(&path, declarations);
        } else if path.extension().is_some_and(|extension| extension == "ts") {
            let source = fs::read_to_string(path).expect("read generated TypeScript");
            let declaration = source
                .lines()
                .filter(|line| !line.starts_with("// ") && !line.starts_with("import "))
                .map(str::trim_end)
                .collect::<Vec<_>>()
                .join("\n")
                .trim()
                .to_string();
            if !declaration.is_empty() {
                declarations.push(declaration);
            }
        }
    }
}

fn exported_type_declarations(source: &str) -> BTreeMap<String, String> {
    source
        .lines()
        .enumerate()
        .filter_map(|(line_index, line)| {
            let name = declaration_name(line)?;
            let remainder = source
                .lines()
                .skip(line_index)
                .collect::<Vec<_>>()
                .join("\n");
            let declaration = exported_type_declaration(&remainder, name)?;
            Some((name.to_string(), declaration.to_string()))
        })
        .collect()
}

fn declaration_name(declaration: &str) -> Option<&str> {
    declaration.lines().find_map(|line| {
        let line = line.trim_start();
        let remainder = line
            .strip_prefix("export type ")
            .or_else(|| line.strip_prefix("export interface "))?;
        remainder
            .split(|character: char| !character.is_ascii_alphanumeric() && character != '_')
            .next()
            .filter(|name| !name.is_empty())
    })
}

fn exported_type_declaration<'a>(source: &'a str, name: &str) -> Option<&'a str> {
    let type_prefix = format!("export type {name}");
    let interface_prefix = format!("export interface {name}");
    let start = source
        .find(&type_prefix)
        .or_else(|| source.find(&interface_prefix))?;
    let declaration = &source[start..];
    let end = declaration.find(';')? + 1;
    Some(&declaration[..end])
}

fn normalize_type_declaration(declaration: &str) -> String {
    let mut normalized = declaration.split_whitespace().collect::<Vec<_>>().join(" ");
    while normalized.contains(" | null | null") {
        normalized = normalized.replace(" | null | null", " | null");
    }
    normalized
}

fn referenced_type_names(source: &str, candidates: &BTreeSet<String>) -> BTreeSet<String> {
    #[derive(Clone, Copy)]
    enum State {
        Code,
        LineComment,
        BlockComment,
        String(char),
    }

    let mut references = BTreeSet::new();
    let mut token = String::new();
    let mut state = State::Code;
    let mut characters = source.chars().peekable();
    while let Some(character) = characters.next() {
        match state {
            State::Code if character == '/' && characters.peek() == Some(&'/') => {
                characters.next();
                if candidates.contains(&token) {
                    references.insert(std::mem::take(&mut token));
                }
                token.clear();
                state = State::LineComment;
            }
            State::Code if character == '/' && characters.peek() == Some(&'*') => {
                characters.next();
                if candidates.contains(&token) {
                    references.insert(std::mem::take(&mut token));
                }
                token.clear();
                state = State::BlockComment;
            }
            State::Code if matches!(character, '\'' | '"' | '`') => {
                if candidates.contains(&token) {
                    references.insert(std::mem::take(&mut token));
                }
                token.clear();
                state = State::String(character);
            }
            State::Code if character.is_ascii_alphanumeric() || character == '_' => {
                token.push(character);
            }
            State::Code => {
                if candidates.contains(&token) {
                    references.insert(std::mem::take(&mut token));
                }
                token.clear();
            }
            State::LineComment if character == '\n' => state = State::Code,
            State::BlockComment if character == '*' && characters.peek() == Some(&'/') => {
                characters.next();
                state = State::Code;
            }
            State::String(_) if character == '\\' => {
                characters.next();
            }
            State::String(quote) if character == quote => state = State::Code,
            State::LineComment | State::BlockComment | State::String(_) => {}
        }
    }
    if candidates.contains(&token) {
        references.insert(token);
    }
    references
}

fn check_or_write(path: &Path, expected: &str, check: bool) {
    if check {
        match fs::read_to_string(path) {
            Ok(actual) if actual == expected => {
                eprintln!("{} is up to date", path.display());
                return;
            }
            Ok(_) | Err(_) => {
                eprintln!(
                    "{} is out of date; run the Agent Runtime contract generator",
                    path.display()
                );
                std::process::exit(1);
            }
        }
    }

    fs::create_dir_all(path.parent().expect("generated parent")).expect("create generated parent");
    fs::write(path, expected).expect("write generated contract");
    eprintln!("wrote {}", path.display());
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn identical_exported_contracts_can_be_shared() {
        let backbone = exported_type_declarations("export type Shared = { value: string, };\n");
        let runtime = "// generated\nexport type Shared = {\n value: string,\n};\n";
        let candidate = exported_type_declaration(runtime, "Shared").expect("runtime declaration");
        assert_eq!(
            normalize_type_declaration(candidate),
            normalize_type_declaration(backbone.get("Shared").expect("backbone declaration"))
        );
    }

    #[test]
    fn same_name_with_a_different_contract_is_detected() {
        let backbone = exported_type_declarations("export type Shared = { value: string, };\n");
        let runtime = "export type Shared = { value: number, };\n";
        let candidate = exported_type_declaration(runtime, "Shared").expect("runtime declaration");
        assert_ne!(
            normalize_type_declaration(candidate),
            normalize_type_declaration(backbone.get("Shared").expect("backbone declaration"))
        );
    }

    #[test]
    fn imports_ignore_comments_and_string_literals() {
        let candidates = ["BackboneEvent", "Turn"]
            .into_iter()
            .map(str::to_string)
            .collect();
        let references = referenced_type_names(
            "export type Carrier = { event: BackboneEvent, kind: \"Turn\", /* Turn */ }; // Turn",
            &candidates,
        );
        assert_eq!(
            references,
            ["BackboneEvent".to_string()].into_iter().collect()
        );
    }
}
